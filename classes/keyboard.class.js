/**
 * Class representing the current state of the keyboard.
 * Tracks which keys are currently pressed for game control.
 */
class Keyboard {
    /** @type {boolean} Flag indicating if the left arrow key is pressed */
    LEFT = false;

    /** @type {boolean} Flag indicating if the right arrow key is pressed */
    RIGHT = false;

    /** @type {boolean} Flag indicating if the up arrow key is pressed */
    UP = false;

    /** @type {boolean} Flag indicating if the down arrow key is pressed */
    DOWN = false;

    /** @type {boolean} Flag indicating if the spacebar is pressed */
    SPACE = false;
}
